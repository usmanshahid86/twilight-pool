"use client";

import Button from "@/components/button";
import { truncateHash } from '@/lib/helpers';
import BTC from "@/lib/twilight/denoms";
import { TransactionHistory } from "@/lib/types";
import { ColumnDef } from "@tanstack/react-table";
import Big from "big.js";
import { ArrowUpRight } from 'lucide-react';
import Link from "next/link";

export function convertDate(toParse: Date | string) {
  return typeof toParse === "string" ? new Date(toParse) : toParse;
}

export const transactionHistoryColumns: ColumnDef<TransactionHistory, any>[] = [
  {
    accessorKey: "date",
    header: "Date & Time",
    accessorFn: (row) => convertDate(row.date).toLocaleString(),
  },
  {
    accessorKey: "value",
    header: "Amount (BTC)",
    accessorFn: (row) =>
      new BTC("sats", Big(row.value)).convert("BTC").toString(),
    enableSorting: true,
  },
  {
    accessorKey: "type",
    header: "Type",
  },
  {
    accessorKey: "fromTag",
    header: "From Account",
    accessorFn: (row) =>
      row.fromTag === "main" ? "Trading Account" : row.fromTag,
  },
  {
    accessorKey: "toTag",
    header: "To Account",
    accessorFn: (row) => (row.toTag === "main" ? "Trading Account" : row.toTag),
  },
  {
    accessorKey: "tx_hash",
    header: "Transaction Hash",
    cell: (row) => (
      <Button className="justify-end gap-0 items-start" asChild variant="link">
        <Link
          href={`${process.env.NEXT_PUBLIC_EXPLORER_URL as string}/tx/${row.getValue()}`}
          target="_blank"
        >
          {truncateHash(row.getValue() as string)} <ArrowUpRight className="h-3 w-3" />
        </Link>
      </Button>
    ),
  },
];
