"use client";
import WalletRegistrationForm from "@/app/_components/registration/form";
import Stepper from "@/components/stepper";
import { Text } from "@/components/typography";
import { useToast } from "@/lib/hooks/useToast";
import useVerifyStatus from "@/lib/hooks/useVerifyStatus";
import { WalletStatus } from "@cosmos-kit/core";
import { useWallet } from "@cosmos-kit/react-lite";
import { AlertCircle, CheckCircle2, Clock, Wallet } from "lucide-react";
import { useRouter } from "next/navigation";
import React, { useEffect } from "react";

const registrationSteps = [
  { id: "register", label: "Register" },
  { id: "verify", label: "Verify" },
  { id: "complete", label: "Complete" },
];

const importantNotices = [
  {
    icon: Wallet,
    text: "Register the Bitcoin address from which you plan to make deposits so funds can be directed to your account properly.",
  },
  {
    icon: Clock,
    text: "Complete the verification step by making a deposit from this address within 72 hours.",
  },
  {
    icon: CheckCircle2,
    text: "Once verified, you'll be able to deposit and trade on the platform.",
  },
];

const Page = () => {
  const { isVerified } = useVerifyStatus();
  const { toast } = useToast();
  const router = useRouter();
  const { status } = useWallet();

  const isWalletConnected = status === WalletStatus.Connected;

  useEffect(() => {
    if (isVerified !== undefined && isVerified === false) {
      toast({
        title: "Verification required",
        description:
          "Please verify your passport to continue with registration",
      });

      router.push("/verify-region");
    }
  }, [isVerified, router, toast]);

  return (
    <div className="flex h-full w-full flex-col px-4 md:px-0">
      <div className="mx-auto my-8 flex w-full max-w-4xl flex-col gap-8 sm:my-12">
        {/* Header with stepper */}
        <div className="flex flex-col items-center gap-6">
          <div className="text-center">
            <Text heading="h1" className="text-3xl font-semibold sm:text-4xl">
              BTC Address Registration
            </Text>
            <Text className="mt-2 text-primary-accent">
              Register your Bitcoin address to start trading on Twilight
            </Text>
          </div>
          <Stepper
            steps={registrationSteps}
            currentStep={1}
            connectorWidth="w-24"
          />
        </div>

        {/* Wallet connection warning */}
        {!isWalletConnected && (
          <div className="flex items-center gap-3 rounded-lg border border-yellow-500/30 bg-yellow-500/10 p-4">
            <AlertCircle className="h-5 w-5 shrink-0 text-yellow-500" />
            <div>
              <Text className="font-medium text-yellow-500">
                Wallet not connected
              </Text>
              <Text className="text-sm text-yellow-500/80">
                Please connect your wallet to register your Bitcoin address.
              </Text>
            </div>
          </div>
        )}

        {/* Main content */}
        <div className="grid gap-8 md:grid-cols-2">
          {/* Form section */}
          <div className="rounded-lg border bg-background p-6">
            <WalletRegistrationForm />
          </div>

          {/* Important notices section */}
          <div className="flex flex-col gap-4 rounded-lg border bg-background p-6">
            <Text heading="h3" className="flex items-center gap-2 font-medium">
              <AlertCircle className="h-5 w-5 text-theme" />
              Important Information
            </Text>
            <div className="space-y-4">
              {importantNotices.map((notice, index) => (
                <div key={index} className="flex gap-3">
                  <div className="flex h-8 w-8 shrink-0 items-center justify-center rounded-full bg-theme/10">
                    <notice.icon className="h-4 w-4 text-theme" />
                  </div>
                  <Text className="text-sm text-primary-accent leading-relaxed">
                    {notice.text}
                  </Text>
                </div>
              ))}
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Page;
